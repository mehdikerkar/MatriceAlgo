package pack;

public class List3 {
	
	int i;
	int j; 
	double value;
	public List3(int i, int j, double value) {
		this.i=i;
		this.j=j;
		this.value=value;
	}
	public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
	public int getJ() {
		return j;
	}
	public void setJ(int j) {
		this.j = j;
	}
	public double getValue() {
		return value;
	}
	public void setValue(double value) {
		this.value = value;
	}
	


}
